package dataBaseModel;

public class Types {

	
	private long id;
	private String name;
	private long post_count;
	private String username;
	private long types_users_id;
	private String date_time;
	private String created;
	private String updated;
	
	
	
	
	
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public long getPost_count() {
		return post_count;
	}
	public void setPost_count(long post_count) {
		this.post_count = post_count;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public long getTypes_users_id() {
		return types_users_id;
	}
	public void setTypes_users_id(long types_users_id) {
		this.types_users_id = types_users_id;
	}
	public String getDate_time() {
		return date_time;
	}
	public void setDate_time(String date_time) {
		this.date_time = date_time;
	}
	public String getCreated() {
		return created;
	}
	public void setCreated(String created) {
		this.created = created;
	}
	public String getUpdated() {
		return updated;
	}
	public void setUpdated(String updated) {
		this.updated = updated;
	}
	
	
	
	
		
}
