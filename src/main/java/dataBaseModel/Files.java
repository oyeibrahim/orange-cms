package dataBaseModel;

public class Files {

	
	private long id;
	private String type;
	private String extension;
	private String path;
	private String name;
	private String alt;
	private long usage_count;
	private String username;
	private long files_users_id;
	private String date_time;
	private String created;
	private String updated;
	
	
	
	
	
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getExtension() {
		return extension;
	}
	public void setExtension(String extension) {
		this.extension = extension;
	}
	public String getPath() {
		return path;
	}
	public void setPath(String path) {
		this.path = path;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAlt() {
		return alt;
	}
	public void setAlt(String alt) {
		this.alt = alt;
	}
	public long getUsage_count() {
		return usage_count;
	}
	public void setUsage_count(long usage_count) {
		this.usage_count = usage_count;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public long getFiles_users_id() {
		return files_users_id;
	}
	public void setFiles_users_id(long files_users_id) {
		this.files_users_id = files_users_id;
	}
	public String getDate_time() {
		return date_time;
	}
	public void setDate_time(String date_time) {
		this.date_time = date_time;
	}
	public String getCreated() {
		return created;
	}
	public void setCreated(String created) {
		this.created = created;
	}
	public String getUpdated() {
		return updated;
	}
	public void setUpdated(String updated) {
		this.updated = updated;
	}
	

	
	
	
	
		
}
